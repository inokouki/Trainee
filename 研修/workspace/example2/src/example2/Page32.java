package example2;

public class Page32 {
	public static void main(String args[]){
	int a;
	a = 6;
	a += 5;

	int b;
	b = 10;
	b -= 6;

	int c;
	c = 2;
	c *= a;

	int d;
	d = 9;
	d /= 3;

	int e;
	e = 7;
	e %= 2;

	int f;
	f = 3;
	f++;

	int g;
	g = 6;
	g--;;

	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
	System.out.println(f);
	System.out.println(g);
	}
}
